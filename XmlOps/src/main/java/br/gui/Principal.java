    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ArrayList<Disciplina> ld = new ArrayList();
        for (int i = 0; i < Integer.parseInt(jSpinner1.getValue().toString()); i++) {
            ld.add(new Disciplina(new Util().randomName()));
        }
        int id = Integer.parseInt(jTextField1.getText());
        if (ld.isEmpty()) {
            lalunos.add(new Aluno(id, jTextField2.getText(), jTextField3.getText()));
        } else {
            lalunos.add(new Aluno(id, jTextField2.getText(), jTextField3.getText(), ld));
        }

        int cont = Integer.parseInt(jTextField1.getText()) + 1;
        jTextField1.setText("" + cont);
        jTextField2.setText(new br.util.Util().randomRA());
        jTextField3.requestFocus();
        jTextField3.select(0, jTextField3.getText().length());
        jSpinner1.setValue(new java.util.Random().nextInt(7));

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String lista = "";
        for (Aluno laluno : lalunos) {
            lista = lista + laluno.getId() + " | " + laluno.getNome();
            if (laluno.getLdisciplina() != null) {
                for (Disciplina disciplina : laluno.getLdisciplina()) {
                    lista = lista + "\n   +" + disciplina.getNome();
                }
            }
            lista = lista + "\n";
        }
        jTextArea1.setText(lista);


    }//GEN-LAST:event_jButton2ActionPerformed

