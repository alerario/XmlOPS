    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        try {
            // TODO add your handling code here:
            String txt = "";
            txt = new XMLUtil().getSchema(Alunos.class);
            jTextArea2.setText(txt);
            JFileChooser xsdfile = new JFileChooser();
            xsdfile.setFileFilter(new FileNameExtensionFilter("XSD File", "xsd"));
            xsdfile.addChoosableFileFilter(new FileNameExtensionFilter("XSD File", "xsd"));
            xsdfile.setSelectedFile(new File("SchemaAlunos.xsd"));
            xsdfile.showSaveDialog(null);
            File file = xsdfile.getSelectedFile();
            if (file != null) {
                FileWriter writer = new FileWriter(file);
                writer.write(txt);
                writer.close();
            }

        } catch (IOException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        String txt = "";
        JFileChooser xsdf = new JFileChooser();
        xsdf.setDialogTitle("Escolha um .jar ");
        xsdf.setFileFilter(new FileNameExtensionFilter("JAR File", "jar"));
        xsdf.showOpenDialog(null);
        File xsdfile = xsdf.getSelectedFile();
        if (xsdfile != null) {
            VarreJar vj = new VarreJar(xsdfile);

            String class_name = (String) JOptionPane.showInputDialog(null, "Informe o nome da classe", "Selecione a classe",
                    JOptionPane.PLAIN_MESSAGE, null, vj.getClassNames().toArray(), "");

            try {
                // JavaClassLoader lcl = new JavaClassLoader(xsdfile, "br.utfpr.outraclasse.Dados");
                JavaClassLoader lcl = new JavaClassLoader(class_name, xsdfile.toURI().toURL());

                Class classe = lcl.getXMLClass();
                txt = new XMLUtil().getSchema(classe);
                jTextArea2.setText(txt);
            } catch (Exception e) {
                jTextArea2.setText(e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed

